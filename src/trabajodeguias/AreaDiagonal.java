    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // TODO add your handling code here:
        double lado1,lado2, resulArea=0, resulDiagonal=0;
        if(rbtncuadrado.isSelected()){
       
       lado1=Double.parseDouble(txtlado1.getText());
       lado2=Double.parseDouble(txtlado2.getText());
       resulArea=cuadrado(lado1,lado2);
       resulDiagonal=diacuadrado(lado1,lado2);
       txtarea.setText(String.valueOf(resulArea));
       txtdiagonal.setText(String.valueOf(resulDiagonal));
       
        }else if(this.rbtnrombo.isSelected()){
            double diagonal2;
            double diagonal1=Double.parseDouble(this.txtdiagonal1.getText());
            lado1=Double.parseDouble(txtlado1.getText());
       lado2=Double.parseDouble(txtlado2.getText());
       resulArea=cuadrado(lado1,lado2);
        txtarea.setText(String.valueOf(resulArea));
        diagonal2=diagonalRombo(diagonal1,resulArea);
        txtdiagonal.setText(String.valueOf(diagonal2));
        }
    }//GEN-LAST:event_btncalcularActionPerformed

