    public double cuadrado(double lado1, double lado2){
        double area=0;
        
    area=lado1*lado2;
    
    return area;
    }
    public double diacuadrado(double lado1, double lado2){
        double diagonal=0;
        double multi=0;
        multi=(lado1*lado1)+(lado2*lado2);
        diagonal=Math.sqrt(multi);
    
        return diagonal;
    }
    public double diagonalRombo( double diagonal1,double resulArea){
        double diagonal=0;
        double multi=0;
        double resta=0;
        double total=0;
        multi=(4*(resulArea*resulArea));
        resta=diagonal1*diagonal1;
        total=multi-resta;
        diagonal=Math.sqrt(total);
        
        return diagonal;
   
    }
